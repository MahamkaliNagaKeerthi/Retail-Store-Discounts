package com.corejava.assignment;

public class User {
	String name;
	String phonenumber;
	public User(String name, String phonenumber) {
		super();
		this.name = name;
		this.phonenumber = phonenumber;
	}
	public String getName() {
		return name;
	}
	public String getPhonenumber() {
		return phonenumber;
	}
	
}
	